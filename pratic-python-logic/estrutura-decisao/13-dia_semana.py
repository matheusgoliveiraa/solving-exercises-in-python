semana = input('Digite um número: ')
dia_semana = int(semana)

if (dia_semana == 1):
    print('1- Domingo')
elif (dia_semana == 2):
    print('2- Segunda')
elif (dia_semana == 3):
    print('3- Terça-Feira')
elif (dia_semana == 4):
    print('4- Quarta-Feira')
elif (dia_semana == 5):
    print('5- Quinta-Feira')
elif (dia_semana == 6):
    print('6- Sexta-Feira')
elif (dia_semana == 7):
    print('7- Sabádo')
else:
    print('Dia inexistente')